package model;

/**
 *
 * @author NCPC
 */
public class Mountain {
    private String code, mountain, province, description;

    public Mountain(String code, String mountain, String province, String description) {
        this.code = code;
        this.mountain = mountain;
        this.province = province;
        this.description = description;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMountain() {
        return mountain;
    }

    public void setMountain(String mountain) {
        this.mountain = mountain;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "Mountain{" + "code=" + code + ", mountain=" + mountain + ", province=" + province + ", description=" + description + '}';
    }
    
    

}
